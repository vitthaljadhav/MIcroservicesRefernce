package com.example.demo.bean;

public class FindMinAndMax {

	private int min;
	
	private int max;
	

	public FindMinAndMax() {
	}

	public FindMinAndMax(int min, int max) {
		super();
		this.min = min;
		this.max = max;
	}

	public int getMin() {
		return min;
	}

	public void setMin(int min) {
		this.min = min;
	}

	public int getMax() {
		return max;
	}

	public void setMax(int max) {
		this.max = max;
	}
	
	
	
	
}
